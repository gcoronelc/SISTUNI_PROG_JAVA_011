public class MateService {
  
  /**
   * Este servicio permita calcular 
   * la suma de dos numeros.
   * 
   * @param n1 Numero entero.
   * @param n2 Numero entero.
   * @return Retorna la suma de los dos numeros.
   */
  public int sumar(int n1, int n2){
    int s;
    s = n1 + n2;
    return s;
  }
  
  

  
}
