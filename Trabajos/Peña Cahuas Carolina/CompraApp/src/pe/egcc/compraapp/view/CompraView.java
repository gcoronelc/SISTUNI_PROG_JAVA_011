    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
         //Datos 
       String tipo = cboMetodo.getSelectedItem().toString();
       double total = Double.parseDouble(txtMonto.getText());
       
       //Proceso
        MedioPagoModel[] repo = control.procesar(tipo,total);
       //Reporte
            //Crear
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblCompra.getModel();
        tabla.setRowCount(0);
        for (MedioPagoModel r : repo) {
            Object[] rowData = {r.getIndice(),r.getValor()};
            tabla.addRow(rowData);
        }
    }//GEN-LAST:event_btnProcesarActionPerformed

